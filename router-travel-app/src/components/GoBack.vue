export default {
  name: "GoBack",
  methods: {
    goBack() {
      return this.$router.go(-1);
    },
  },
};
</script>
<style scoped>
.go-back {
  display: flex;
}
button {
  border: 0;
  cursor: pointer;
}
</style>
